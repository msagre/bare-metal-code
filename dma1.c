typedef unsigned short pcm_t;

#define MAXVAL  ((pcm_t) ((1 << (sizeof(pcm_t)*8))-1))
#define MAXHALF ((pcm_t)  (1 << (sizeof(pcm_t)*8-1)))

unsigned char fosd (pcm_t pcm, int acc[1], pcm_t fdbk) 
{
	int diff;

	diff =   fdbk;
	diff +=  pcm;
	diff >>= 1;

	acc[0] += diff-MAXHALF;
	return (acc[0] > 0) ? 1 : 0;
}

unsigned char sosd (pcm_t pcm, int acc[2], pcm_t fdbk)
{
	int diff1;
	int diff2;

	diff1 =   fdbk;
	diff1 +=  pcm;
	diff1 >>= 1;

	acc[0] += diff1-MAXHALF;

	diff2  = acc[0];
	diff2 += MAXHALF;
	diff2 += fdbk;
	diff2 >>= 1;

	acc[1] += diff2;
	acc[1] -= MAXHALF;

	return (acc[1] > 0) ? 1 : 0;
}

